/// Simulates `driver_location_updated` by walking [polyline] every [interval].
///
/// Uses the [orderId] from the map screen so filtering in
/// [driverLocationProvider] still works. Heading/rotation is handled in the
/// map layer ([DriverMarkerHeading]), not here.
Stream<DriverLocation> simulateDriverLocationStream({
  required int orderId,
  Duration interval = const Duration(seconds: 2),
  bool loop = true,
}) async* {
  if (polyline.isEmpty) return;

  do {
    for (var i = 0; i < polyline.length; i++) {
      final point = polyline[i];
      yield DriverLocation(orderId: orderId, lat: point[0], lng: point[1]);

      if (i < polyline.length - 1) {
        await Future.delayed(interval);
      }
    }
  } while (loop);
}
